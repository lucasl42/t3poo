package t3poo;


public class User {
    protected String id;
    protected String name;
    protected String birthDate;
    protected String email;
    protected String nbooks;
    protected String issuspended;
    protected int maxBooks;
    protected int maxDays;

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the birthDate
     */
    public String getBirthDate() {
        return birthDate;
    }

    /**
     * @param birthDate the birthDate to set
     */
    public void setBirthDate(String birthDate) {
        this.birthDate = birthDate;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the nbooks
     */
    public String getNbooks() {
        return nbooks;
    }

    /**
     * @param nbooks the nbooks to set
     */
    public void setNbooks(String nbooks) {
        this.nbooks = nbooks;
    }

    /**
     * @return the issuspended
     */
    public String getIssuspended() {
        return issuspended;
    }

    /**
     * @param issuspended the issuspended to set
     */
    public void setIssuspended(String issuspended) {
        this.issuspended = issuspended;
    }

    /**
     * @return the maxBooks
     */
    public int getMaxBooks() {
        return maxBooks;
    }

    /**
     * @return the maxDays
     */
    public int getMaxDays() {
        return maxDays;
    }


    
    
    
}
